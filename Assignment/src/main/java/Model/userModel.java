package Model;

public class userModel {
    private int id;
    private String name;
    private String password;

    public int getId()
    {
        return id;
    }
    public void setId(int value)
    {
        id=value;
    }
    public String getName()
    {
        return name;
    }
    public void setName(String value)
    {
        name = value;
    }
    public String getPassword()
    {
        return password;
    }
    public void setPassword(String value)
    {
        password = value;
    }
}
